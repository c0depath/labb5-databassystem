CREATE OR REPLACE PROCEDURE DO_INSÄTTNING(
    P_PNR IN INSÄTTNING.PNR%TYPE,
    P_KNR IN INSÄTTNING.KNR%TYPE,
    P_BELOPP IN INSÄTTNING.BELOPP%TYPE) 
AS 
V_RADNR NUMBER(6);
BEGIN
SELECT RADNR_SEQ.NEXTVAL 
INTO V_RADNR
FROM DUAL;
