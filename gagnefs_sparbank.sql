-- Uppgift 7
CREATE OR REPLACE FUNCTION LOGGA_IN(
    P_PNR in BANKKUND.PNR%TYPE,
    P_PASSWD in BANKKUND.PASSWD%TYPE) 
RETURN NUMBER
AS
V_PNR BANKKUND.PNR%TYPE;
V_PASSWD BANKKUND.PASSWD%TYPE;
BEGIN
SELECT PNR.PASSWD 
INTO V_PNR, V_PASSWD
FROM BANKKUND
WHERE PNR = P_PNR 
AND PASSWD = P_PASSWD;
RETURN 1;
EXCEPTION
WHEN OTHERS 
THEN 
RETURN 0;
END;

-- Uppgift 8
CREATE OR REPLACE FUNCTION GET_SALDO(
    P_KNR IN KONTO.KNR%TYPE) 
RETURN NUMBER 
AS 
V_SALDO KONTO.SALDO%TYPE;
BEGIN
SELECT SALDO 
INTO V_SALDO
FROM KONTO
WHERE KNR = P_KNR;
RETURN V_SALDO;
EXCEPTION
WHEN no_data_found 
THEN 
RETURN -1;
END;

-- Uppgift 9
CREATE OR REPLACE FUNCTION GET_BEHÖRIGHET(
    p_pnr IN KONTOÄGARE.PNR%TYPE,
    p_knr IN KONTOÄGARE.KNR%TYPE) 
RETURN NUMBER 
AS 
V_SUCCESS NUMBER;
BEGIN
SELECT COUNT(RADNR) 
INTO V_SUCCESS
FROM KONTOÄGARE
WHERE PNR = P_PNR 
AND KNR = P_KNR;
RETURN V_SUCCESS;
END;

